export const CreateList = createAction('[Board] Create List', props<{ name: string }>());
export const ListCreated = createAction('[Board] List Created', props<{ list: List }>());
export const ArchiveList = createAction('[Board] Archive List', props<{ id: string }>());
export const ListArchived = createAction('[Board] List Archived', props<{ id: string }>());
export const ListUpdated = createAction('[Board] List Updated', props<{ list: List }>());
export const RenameList = createAction('[Board] Rename List', props<{ name: string, list: List }>());
export const ToggleSubscribedList = createAction('[Board] Toggle Subscribed List', props<{ list: List }>());
export const MoveList = createAction('[Board] Move List', props<{ list: List, newIndex: number }>());
export const CopyList = createAction('[Board] Copy List', props<{ list: List, name: string }>());
export const CreateCard = createAction('[Board] Create Card', props<{ name: string, idList: string, pos: number }>());
