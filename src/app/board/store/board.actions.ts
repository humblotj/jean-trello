export const UpdateCard = createAction('[Board] Update Card', props<{ card: Card }>());
export const CardUpdated = createAction('[Board] Card Updated', props<{ card: Card }>());
export const CopyCard = createAction('[Board] Copy Card', props<{ card: Card, name: string, idList: string, index: number }>());
export const MoveCard = createAction('[Board] Move Card', props<{ card: Card, idList: string, index: number }>());
export const ArchiveAllCards = createAction('[Board] Archive All Cards', props<{ idList: string }>());
export const SortCards = createAction('[Board] Sort Cards', props<{ idList: string, sortBy: 'newest' | 'oldest' | 'alphabetically' }>());
export const MoveAllCards = createAction('[Board] Move All Cards', props<{ prev: string, next: string }>());
